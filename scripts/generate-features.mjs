// @ts-check
import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');

const pkg = JSON.parse(readFileSync(resolve(ROOT, 'package.json'), 'utf-8'));
const features = /** @type {Record<string, string>} */ (pkg['hbi-aad']?.features ?? {});
const outDir = resolve(ROOT, 'src', 'features');
const outFile = resolve(outDir, 'generated.ts');

const entries = Object.entries(features);

// Build import lines + registration array
const imports = [];
const registrations = [];

for (const [featureName, mountPath] of entries) {
  const varName = featureName.replace(/[^a-zA-Z0-9_]/g, '_');
  imports.push(`import { createRouter as ${varName}Router } from './${featureName}/index.ts';`);
  registrations.push(`  { path: '${mountPath}', mount: (deps) => ${varName}Router(deps) },`);
}

const content = `// Auto-generated by scripts/generate-features.mjs
// Do not edit manually. Run \`npm run generate\` to update.

${imports.join('\n')}

import type { FeatureDeps } from '../core/app.ts';
import type { Hono } from 'hono';

export interface RegisteredFeature {
  path: string;
  mount: (deps: FeatureDeps) => Hono<any, any, any>;
}

export function getFeatures(): RegisteredFeature[] {
  return [
${registrations.join('\n')}  ];
}
`;

if (!existsSync(outDir)) {
  mkdirSync(outDir, { recursive: true });
}
writeFileSync(outFile, content, 'utf-8');

console.log(`✓ Generated ${entries.length} feature(s): ${entries.map(([n, p]) => `${p} ← ${n}`).join(', ')}`);
console.log(`  → ${outFile}`);
