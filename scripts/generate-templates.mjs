// @ts-check
import { readFileSync, writeFileSync, readdirSync, existsSync, mkdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const TEMPLATE_DIR = resolve(ROOT, 'src', 'features', 'actions', 'templates');
const OUT_DIR = resolve(ROOT, 'src', 'features', 'actions');
const OUT_FILE = resolve(OUT_DIR, 'templates.generated.ts');

if (!existsSync(TEMPLATE_DIR)) {
  mkdirSync(TEMPLATE_DIR, { recursive: true });
}

const templates = [];
const templateMetas = [];

for (const filename of readdirSync(TEMPLATE_DIR)) {
  if (!filename.endsWith('.yml') && !filename.endsWith('.yaml')) continue;
  const id = filename.replace(/\.(yml|yaml)$/, '');
  const content = readFileSync(resolve(TEMPLATE_DIR, filename), 'utf-8');

  const nameMatch = content.match(/^name:\s*"?(.+?)"?\s*$/m);
  const descMatch = content.match(/^description:\s*"?(.+?)"?\s*$/m);
  const catMatch = content.match(/^#\s*category:\s*(\w+)\s*$/m);
  const tagMatches = content.match(/^#\s*tags:\s*(.+?)\s*$/m);

  const name = nameMatch ? nameMatch[1] : id;
  const description = descMatch ? descMatch[1] : '';
  const category = catMatch ? catMatch[1] : 'deploy';
  const tags = tagMatches ? tagMatches[1].split(/[\s,]+/).filter(Boolean) : [];

  templateMetas.push({ id, name, description, category, tags });
  templates.push({ id, name, description, category, tags, content });
}

const out = [];
out.push('// Auto-generated by scripts/generate-templates.mjs');
out.push('// Do not edit manually. Run `npm run generate` to update.');
out.push('');
out.push('export interface TemplateMeta {');
out.push('  id: string;');
out.push('  name: string;');
out.push('  description: string;');
out.push("  category: 'ci' | 'deploy' | 'service' | 'maintenance' | 'test';");
out.push('  tags: string[];');
out.push('}');
out.push('');
out.push('export interface Template extends TemplateMeta {');
out.push('  /** Full YAML content. */');
out.push('  content: string;');
out.push('}');
out.push('');
out.push('export const TEMPLATES: Template[] = ' + JSON.stringify(templates, null, 2) + ';');
out.push('');
out.push('export const TEMPLATE_METAS: TemplateMeta[] = ' + JSON.stringify(templateMetas, null, 2) + ';');
out.push('');

if (!existsSync(OUT_DIR)) mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(OUT_FILE, out.join('\n'), 'utf-8');

console.log('Generated ' + templates.length + ' template(s): ' + templates.map(function(t) { return t.id; }).join(', '));
console.log('  -> ' + OUT_FILE);
