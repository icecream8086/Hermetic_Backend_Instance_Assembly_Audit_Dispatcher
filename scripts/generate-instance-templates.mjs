// @ts-check
import { readFileSync, writeFileSync, readdirSync, existsSync, mkdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import yaml from 'js-yaml';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const TEMPLATE_DIR = resolve(ROOT, 'src', 'features', 'template', 'templates');
const OUT_DIR = resolve(ROOT, 'src', 'features', 'template');
const OUT_FILE = resolve(OUT_DIR, 'templates.generated.ts');

if (!existsSync(TEMPLATE_DIR)) {
  mkdirSync(TEMPLATE_DIR, { recursive: true });
}

const templates = [];
const templateMetas = [];

for (const filename of readdirSync(TEMPLATE_DIR)) {
  if (!filename.endsWith('.yml') && !filename.endsWith('.yaml')) continue;
  const id = filename.replace(/\.(yml|yaml)$/, '');
  const raw = readFileSync(resolve(TEMPLATE_DIR, filename), 'utf-8');
  const tpl = yaml.load(raw);

  const name = tpl.name || id;
  const description = tpl.description || '';
  const category = tpl.category || '';
  const tags = Array.isArray(tpl.tags) ? tpl.tags : [];

  const { name: _n, description: _d, category: _c, tags: _t, ...spec } = tpl;

  templateMetas.push({ id, name, description, category, tags });
  templates.push({ id, name, description, category, tags, spec });
}

let out = '';
out += '// Auto-generated by scripts/generate-instance-templates.mjs\n';
out += '// Do not edit manually. Run `npm run generate` to update.\n';
out += '\n';
out += 'export interface InstanceTemplateMeta {\n';
out += '  id: string;\n';
out += '  name: string;\n';
out += '  description: string;\n';
out += '  category: string;\n';
out += '  tags: string[];\n';
out += '}\n';
out += '\n';
out += 'export interface InstanceTemplateDef {\n';
out += '  id: string;\n';
out += '  name: string;\n';
out += '  description: string;\n';
out += '  category: string;\n';
out += '  tags: string[];\n';
out += '  spec: Record<string, unknown>;\n';
out += '}\n';
out += '\n';
out += 'export const INSTANCE_TEMPLATES: InstanceTemplateDef[] = ' + JSON.stringify(templates, null, 2) + ';\n';
out += '\n';
out += 'export const INSTANCE_TEMPLATE_METAS: InstanceTemplateMeta[] = ' + JSON.stringify(templateMetas, null, 2) + ';\n';

if (!existsSync(OUT_DIR)) mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(OUT_FILE, out, 'utf-8');

console.log('Generated ' + templates.length + ' instance template(s): ' + templates.map(function(t) { return t.id; }).join(', '));
console.log('  -> ' + OUT_FILE);
